@dataclass(frozen=True)
class OpinionQualityRule:
    category: str
    tag: str
    level: str
    term: str
    issue: str
    replacement: str
    patterns: tuple[str, ...]


@dataclass(frozen=True)
class OpinionQualityMatch:
    category: str
    tag: str
    level: str
    term: str
    issue: str
    replacement: str


def _rule(
    category: str,
    tag: str,
    level: str,
    term: str,
    issue: str,
    replacement: str,
    patterns: tuple[str, ...],
) -> OpinionQualityRule:
    return OpinionQualityRule(
        category=category,
        tag=tag,
        level=level,
        term=term,
        issue=issue,
        replacement=replacement,
        patterns=patterns,
    )


